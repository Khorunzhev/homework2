DROP TABLE IF EXISTS BOOK;
DROP TABLE IF EXISTS AUTHOR;
DROP TABLE IF EXISTS GENRE;

CREATE TABLE AUTHOR(ID BIGINT PRIMARY KEY AUTO_INCREMENT NOT NULL, FULLNAME VARCHAR(255));
CREATE TABLE GENRE(ID BIGINT PRIMARY KEY AUTO_INCREMENT NOT NULL, NAME VARCHAR(255));

CREATE TABLE BOOK(
    ID BIGINT PRIMARY KEY AUTO_INCREMENT NOT NULL,
    TITLE VARCHAR(255),
    CONSTRAINT FK_AUTHOR_ID FOREIGN KEY (ID) REFERENCES AUTHOR(ID),
    CONSTRAINT FK_GENRE_ID FOREIGN KEY (ID) REFERENCES GENRE(ID));
